let pi = 3.14159265359;
console.log("Pi: ", pi);

//This is the value of the diameter:
let d = 10;
console.log("Diameter: ", d);

//This is the value of the radius:

let r = d / 2;
console.log("Radius: ", r);

//This is the value of the radius squared:

let r2 = r * r;
console.log("Radius squared: ", r2);

//The result is:
let area = pi * r2;
console.log("Area: ", area);
