function sum(x, y) {
    let result = x + y;
    console.log(`The result of ${x} + ${y} is the ${result}`)
    return result;
}

sum(20, 5);

function substraction(a, b) {
    let resultOfSub = a - b;
    console.log(`The result of ${a} - ${b} is the ${resultOfSub}`);
    return resultOfSub;
}

substraction(20, 10);

function multiplication(z, t) {
    let resultOfMultiplication = z * t;
    console.log(`The result of multiplication is ${z} * ${t} that gives the ${resultOfMultiplication}`);
    return resultOfMultiplication;
}

multiplication(5, 5);

function division(c, d) {
    let resultOfDivision = c / d;
    console.log(`The result is ${resultOfDivision} cause ${c} / ${d} `);
    return resultOfDivision;
}

division(20, 5);